RARITY_WEIGHT = {
    "legendary": 5,
    "epic":      15,
    "rare":      35,
    "uncommon":  60,
    "common":    100,
}


def get_personality(character_id: str) -> dict | None:
    return PERSONALITIES.get(character_id)


def search_personality(query: str) -> dict | None:
    q = query.lower().strip()
    for char_id, char in PERSONALITIES.items():
        if char["name"].lower() == q or char_id == q:
            return {**char, "id": char_id}
    for char_id, char in PERSONALITIES.items():
        if q in char["name"].lower() or q in char_id:
            return {**char, "id": char_id}
    return None


def roll_weighted() -> tuple[str, dict]:
    import random
    ids = list(PERSONALITIES)
    weights = [RARITY_WEIGHT[PERSONALITIES[c]["rarity"]] for c in ids]
    char_id = random.choices(ids, weights=weights, k=1)[0]
    return char_id, PERSONALITIES[char_id]
